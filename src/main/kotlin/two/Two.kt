        private fun isSafe(report: IntArray): Boolean {
            var i = 0
            val higher = report[0] < report[1]
            var correct: Boolean

            do {
                correct = if (higher) report[i + 1] - report[i] in 1..3 else report[i + 1] - report[i] in -3..-1
                i++
            } while (i < report.size - 1 && correct)
            return correct
        }

        private fun firstUnsafe(report: IntArray): Int {
            var i = 0
            val higher = report[0] < report[1]
            var correct: Boolean

            do {
                val value = if (higher) report[i + 1] - report[i] else abs(report[i + 1] - report[i])
                correct = value in 1..3
                if (!correct) return i
                i++
            } while (i < report.size - 1)
            return i;
        }

